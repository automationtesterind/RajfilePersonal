package basic.Programs.java;

public class Global_Variables {
	int a;
	String Name;
	float f;
	double d;
	boolean b;

	public static void main(String[] args) {
		Global_Variables GB = new Global_Variables();
		System.out.println(GB.a);
		System.out.println(GB.Name);
		System.out.println(GB.f);
		System.out.println(GB.d);
		System.out.println(GB.b);
	}

}
