package basic.Programs.java;

public class looping {

	public static void main(String[] args) {
		// Write the for loop that run from -10 to -20
		for (int i = -10; i >= -20; i--) {
			System.out.println(i);
		}
		System.out.println("*********");

		// "Write the for loop that run from 10 to -50 "
		for (int j = 10; j >= -50; j--) {
			System.out.println(j);
		}

	}

}
